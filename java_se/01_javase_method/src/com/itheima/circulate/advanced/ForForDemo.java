package com.itheima.circulate.advanced;

public class ForForDemo {
    public static void main(String[] args) {
        //嵌套循环
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j <4; j++) {
                System.out.print("*");
            }
            System.out.println();

        }
    }
}
